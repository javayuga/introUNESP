/*
----------------- MATRIZES ------------------

  De maneira geral, as matrizes são vetores com 
mais de uma dimensão. Assim, ao invés de ter só
uma linha, a matriz é um conjunto de vetores or-
ganizados em colunas.
  
  Para criar uma matriz, devemos declarar a quan-
tidade de linhas e de colunas. Por exemplo:
       
       int main()
       {
         int campo_minado[3][3];
       }
  
  A matriz do exemplo acima é de ordem 3x3 do tipo
int. De forma geral, temos que declarar as linhas,
depois as colunas.
  
*/
